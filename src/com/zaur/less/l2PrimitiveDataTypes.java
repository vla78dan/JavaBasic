package com.zaur.less;

public class l2PrimitiveDataTypes {
    public static void main(String[] args) {
        byte b1 = 10;
        byte b2 = 20;
        byte b3 = 100;

        short s1 = 5;
        short s2 = -10;
        short s3 = 0;

        int i1 = 500;

        long l1 = 1000;
        long l2 = 20000000000L;

        float f1 = 3.14f;
        float f2 = 2.5f;
        float f3 = 20;

        double d1 = 3.14D;
        double d2 = 3.14d;

        char c1 = 'a';
        char c2 = 'A';
        char c5 = 300;
        char c6 = '\u0500';

        System.out.println(c6);
    }
}
